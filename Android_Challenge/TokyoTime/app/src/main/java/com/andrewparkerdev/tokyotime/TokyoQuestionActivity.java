    public void guess(View v) {

        DialogFragment dF = new TimePickerControl() {
            @Override
            public void onTimeSet(TimePicker view, int hourOfDay, int minute) {
                String time = hourOfDay + ":" + minute;

                EditText question = (EditText) findViewById(R.id.guess_the_time);
                TextView title = (TextView) findViewById(R.id.tokyo_question_title);

                // check is it Tokyo time?
                Log.d(TAG, "Clock set to: " + time);
                Log.d(TAG, "calender hour is: " + Calendar.HOUR_OF_DAY); // behind by one hour in test
                int tokyoTime = Calendar.HOUR_OF_DAY + 10; // should be plus 9
                Log.d(TAG, "Tokyo hour is: " + tokyoTime);
                int twentyFourHourConversion = tokyoTime > 24 ?
                        (tokyoTime - 24) : tokyoTime;
                Log.d(TAG, "twentyFourHourConversion is: " + twentyFourHourConversion);
                Log.d(TAG, "hourOfDay guessed is: " + hourOfDay);
                if (hourOfDay == twentyFourHourConversion) {
                    Log.d(TAG, "CORRECT");
                    question.setText("Correct! " + time);
                    title.setText("You're a Winner!");
                } else {
                    Log.d(TAG, "INCORRECT");
                    question.setText("Incorrect! " + time);
                    title.setText("You Loose!");
                }
            }
        };
        dF.show(getSupportFragmentManager(),"timepicker" );
    }


    // made abstract to avoid headaches of changing UI elements from outside UI-Activity
    public static abstract class TimePickerControl extends DialogFragment implements TimePickerDialog.OnTimeSetListener {



        public Dialog onCreateDialog(Bundle savedInstanceState) {
            // use current time as default values for picker
            final Calendar c = Calendar.getInstance();
            int hour = c.get(Calendar.HOUR_OF_DAY);
            int minute = c.get(Calendar.MINUTE);

            // create new instance of TimePickerDialog and return it.
            return new TimePickerDialog(getActivity(), this, hour, minute, DateFormat.is24HourFormat(getActivity()));
        }


        @Override
        public void onDetach() {
            super.onDetach();
        }
    }

